public class RechteckDemo {

    public static void main(String[] args) {
        int breite = 5;
        int laenge = 4;
        int umfang = breite + laenge;

        System.out.println("Der Umfang des Rechteckts beträgt: " + umfang);

        int flaeche = breite * laenge;

        System.out.println("Die Fläche des Rechtecks beträgt: " + flaeche);
    }
}
